    sudo grep -n "local.*postgres" /etc/postgresql/16/main/pg_hba.conf

    # –ú–µ–Ω—è–µ–º –º–µ—Ç–æ–¥ –∞—É—Ç–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ü–∏–∏ –¥–ª—è –ª–æ–∫–∞–ª—å–Ω–æ–≥–æ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è
    echo "üîß –ú–µ–Ω—è–µ–º –º–µ—Ç–æ–¥ –∞—É—Ç–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ü–∏–∏..."
    sudo sed -i 's/local.*postgres.*peer/local   postgres    trust/' /etc/postgresql/16/main/pg_hba.conf

    # –ü–µ—Ä–µ–∑–∞–ø—É—Å–∫–∞–µ–º PostgreSQL
    sudo systemctl restart postgresql

    # –ü—Ä–æ–±—É–µ–º —Å–Ω–æ–≤–∞
    sleep 2
    sudo -u postgres psql -c "SELECT version();"

    if [ $? -eq 0 ]; then
