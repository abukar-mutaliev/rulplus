        sudo -u postgres psql -c "ALTER USER postgres PASSWORD 'postgres_password_2024';"

        # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç–∏
