    echo ""
    echo "üìù DATABASE_URL –¥–ª—è .env —Ñ–∞–π–ª–∞:"
    echo "DATABASE_URL=\"postgresql://postgres:postgres_password_2024@localhost:5432/rulplus_db\""
    echo ""
    echo "üìÑ –û–±–Ω–æ–≤–∏—Ç–µ –≤–∞—à .env —Ñ–∞–π–ª –∏ –ø–æ–ø—Ä–æ–±—É–π—Ç–µ —Å–Ω–æ–≤–∞:"
